#  Lookup functions
# ══════════════════════════════════════════════════════════════════════════════

def get_cv(table: dict, model: int, k: float, T: int = 500):
    """Lookup critical values with nearest-match fallback."""
    if model not in table:
        model = min(table.keys(), key=lambda m: abs(m - model))

    subtable = table[model]

    # If subtable is {k: {T: {cv}}} structure
    if isinstance(subtable, dict):
        first_val = next(iter(subtable.values()))
        if isinstance(first_val, dict):
            # Check if nested by T
            first_inner = next(iter(first_val.values()))
            if isinstance(first_inner, dict) and "1%" in first_inner:
                # {k: {T: {cv}}} — find nearest k then nearest T
                k_keys = sorted(subtable.keys())
                k_near = min(k_keys, key=lambda x: abs(x - k))
                t_table = subtable[k_near]
                t_keys = sorted(t_table.keys())
                t_near = min(t_keys, key=lambda x: abs(x - T))
                return t_table[t_near]
            elif "1%" in first_val:
                # {k: {cv}} — find nearest k
                k_keys = sorted(subtable.keys())
                k_near = min(k_keys, key=lambda x: abs(x - k))
                return subtable[k_near]
    return {"1%": np.nan, "5%": np.nan, "10%": np.nan}


def qr_kss_cv(model: int, T: int, tau: float, level: str = "5%"):
    """
    Compute QR-KSS critical value from response surface polynomial.
    Li & Park (2018), Table 1.
    """
    if model not in QR_KSS_RESPONSE:
        model = 1
    tau_table = QR_KSS_RESPONSE[model]
    if tau not in tau_table:
        taus = sorted(tau_table.keys())
        tau = min(taus, key=lambda t: abs(t - tau))
    c = tau_table[tau]
    return c[0] + c[1] / T + c[2] / (T ** 2)


def qr_kss_delta2_cv(delta2: float, model: int = 1):
    """
    QR-KSS critical values using delta^2-based interpolation.
    Matches GAUSS crit_qr_kss() procedure exactly.

    Parameters
    ----------
    delta2 : float
        Normalized delta^2 in [0, 1].
    model : int
        0 or 1 = constant, 2 = constant+trend.

    Returns
    -------
    cv_1, cv_5, cv_10 : float
        Critical values at 1%, 5%, 10%.
    """
    # model 0 and 1 both use the "constant" table
    m = model if model in QR_KSS_CV_TABLE else 1
    crt = QR_KSS_CV_TABLE[m]

    if delta2 < 0.1:
        ct = crt[0]
    else:
        r210 = delta2 * 10
        if r210 >= 10:
            ct = crt[9]
        else:
            r2a = int(np.floor(r210))  # 1-indexed row in GAUSS, 0-indexed here
            r2b = int(np.ceil(r210))
            if r2a == r2b or r2a < 1:
                ct = crt[max(r2a - 1, 0)]
            else:
                wa = r2b - r210
                # GAUSS uses 1-indexed: crt[r2a,.], Python 0-indexed: crt[r2a-1]
                ct = [wa * crt[r2a - 1][i] + (1 - wa) * crt[r2b - 1][i]
                      for i in range(3)]

    return ct[0], ct[1], ct[2]

